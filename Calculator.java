class Calculator {
	
	int add(int a, int b) {
		int result = a + b;
		return result;
	}
	
	int sub(int a, int b) {
		int result = a - b;
		return result;
	}
	
	int multiply(int a, int b) {
		int result = a * b;
		return result;
	}
	
	double div(int a, int b) {
		double a1 = a;
		double b1 = b;
		double result = a1 / b1;
		return result;
	}
}
